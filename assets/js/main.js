function darkMode() {
    let element = document.body
    element.classList.toggle("dark-mode")
    document.getElementById('Img1').src = 'assets/img/darkmode-img4.png'
    document.getElementById('Img2').src = 'assets/img/darkmode-img5.png'
    document.getElementById('Img3').src = 'assets/img/darkmode-img6.png'
}